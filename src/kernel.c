char boot_messages[80 * 25] = {0};
int boot_messages_len = 0;
int current_line = 0;
int current_column = 0;
unsigned int system_memory_lower = 0;
unsigned int system_memory_upper = 0;

typedef struct {
    unsigned int size;
    unsigned int base_low;
    unsigned int base_high;
    unsigned int len_low;
    unsigned int len_high;
    unsigned int type;
} __attribute__((packed)) mmap_entry_t;

void printf(const char* str)
{
    for (int i = 0; str[i] != 0; i++) {
        if (str[i] == '\n') {
            current_line++;
            current_column = 0;
            
            if (current_line >= 25) {
                for (int line = 0; line < 24; line++) {
                    for (int col = 0; col < 80; col++) {
                        boot_messages[line * 80 + col] = boot_messages[(line + 1) * 80 + col];
                    }
                }
                for (int col = 0; col < 80; col++) {
                    boot_messages[24 * 80 + col] = 0;
                }
                current_line = 24;
            }
        }
        else {
            if (current_column < 80) {
                boot_messages[current_line * 80 + current_column] = str[i];
                current_column++;
                
                if (current_column >= 80) {
                    current_line++;
                    current_column = 0;
                    
                    if (current_line >= 25) {
                        for (int line = 0; line < 24; line++) {
                            for (int col = 0; col < 80; col++) {
                                boot_messages[line * 80 + col] = boot_messages[(line + 1) * 80 + col];
                            }
                        }
                        for (int col = 0; col < 80; col++) {
                            boot_messages[24 * 80 + col] = 0;
                        }
                        current_line = 24;
                    }
                }
            }
        }
    }
    
    boot_messages_len = 0;
    for (int i = 0; i < 80 * 25; i++) {
        if (boot_messages[i] != 0) {
            boot_messages_len = i + 1;
        }
    }
}

void print_hex(unsigned int n)
{
    char* foo = "0x00000000";
    char* hex = "0123456789ABCDEF";
    foo[2] = hex[(n >> 28) & 0xF];
    foo[3] = hex[(n >> 24) & 0xF];
    foo[4] = hex[(n >> 20) & 0xF];
    foo[5] = hex[(n >> 16) & 0xF];
    foo[6] = hex[(n >> 12) & 0xF];
    foo[7] = hex[(n >> 8) & 0xF];
    foo[8] = hex[(n >> 4) & 0xF];
    foo[9] = hex[n & 0xF];
    printf(foo);
}

void set_timer_freq(unsigned int divisor)
{
    outb(0x43, 0x36);
    outb(0x40, divisor & 0xFF);
    outb(0x40, divisor >> 8);
}

void attempt_shutdown()
{
    acpi_poweroff();
}

void kernel_main(const void* multiboot_struct)
{
    // INITIALIZE

    printf("lasang please\n");
    init_gdt();
    enable_sse();
    unsigned int* header = (unsigned int*)(((unsigned int)multiboot_struct) + 8);
    
    // MEMORY INFO

    unsigned int* mb_header = (unsigned int*)multiboot_struct;
    
    if (mb_header[0] & (1 << 6)) {
        unsigned int mmap_length = mb_header[11];
        unsigned int mmap_addr = mb_header[12];
        
        printf("Using memory map for detection\n");
        
        unsigned int total_memory_kb = 0;
        mmap_entry_t* mmap = (mmap_entry_t*)mmap_addr;
        unsigned int mmap_end = mmap_addr + mmap_length;
        
        while ((unsigned int)mmap < mmap_end) {
            if (mmap->type == 1) {
                unsigned int region_kb = mmap->len_low / 1024;
                if (mmap->len_high > 0) {
                    region_kb = 0xFFFFFFFF / 1024;
                }
                total_memory_kb += region_kb;
            }
            mmap = (mmap_entry_t*)((unsigned int)mmap + mmap->size + 4);
        }
        
        system_memory_lower = 640;
        system_memory_upper = total_memory_kb - 1024;
        
        printf("Total usable RAM: ");
        print_hex(total_memory_kb);
        printf(" KB (");
        print_hex(total_memory_kb / 1024);
        printf(" MB)\n");
    }
    else if (mb_header[0] & 0x1) {
        system_memory_lower = mb_header[1];
        system_memory_upper = mb_header[2];
        printf("Memory (basic): lower=");
        print_hex(system_memory_lower);
        printf(" upper=");
        print_hex(system_memory_upper);
        printf("\n");
    }
    else {
        printf("Memory info not available from multiboot\n");
        system_memory_lower = 640;
        system_memory_upper = 130304; // 128MB - 1MB
    }

    // INTERNAL TIMER

    set_timer_freq(1193);
    init_idt();

    // USER INTERFACE

    unsigned char* buffer = (unsigned char*)header[20];
    init_graphics(buffer);
    init_acpi();
    init_mouse();
    init_serial();
    init_usb();
    init_storage(2048);

    system_draw_screen(0, 0, 0);
    system_draw_image(WIDTH / 2 - 75, HEIGHT / 2 - 30, 150, 60, mlogo_60, 255, 255, 255);
    invalidate_buffer();
    sleep(1000);
    init_desktop();

    // DESKTOP CLOSED

    system_draw_screen(0, 0, 0);
    system_draw_image(WIDTH / 2 - 75, HEIGHT / 2 - 30, 150, 60, mlogo_60, 255, 255, 255);
    system_draw_text(10, 26, "Attempting shutdown...", 24, 0xdb, 0x81, 0x29);
    system_draw_text(WIDTH / 2 - 50, HEIGHT / 2 + 60, "Goodbye!", 24, 255, 255, 255);
    invalidate_buffer();

    // DISABLE INTERRUPTS

    sleep(1000);
    outb(0x21, 0xFF);
    attempt_shutdown();
}
